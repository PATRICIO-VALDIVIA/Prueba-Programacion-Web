        <h1 style="color:white">Series Exitosas</h1>
            <p style="color:white"> Death Note: Con apenas 37 episodios Tsugumi Ōba y Takeshi Obata supieron crear uno de los anime más exitosos
                y controversiales del mundo. Aunque el bachiller Light Yagami sólo quería eliminar a criminales con la ayuda d
                e un cuaderno mágico, para otros esta herramienta fue una excusa para asesinar a mansalva. Algunos recordarán
                los casos de Bélgica y Alabama relacionados con el anime. En el primero se encontró una nota en japonés 
                cerca de los restos de un hombre asesinado y en el segundo, dos estudiantes de 6to grado llevaban un 
                Death note con el nombre de varios de sus compañeros de clases.¿Escalofriante, no?
            </p>
           
            
            <p style="color:white"> Dragon Ball Z: Cerca de 300 episodios fueron transmitidos en el mundo entero, entre 1986 y 1996. 
                El anime creado por Akira Toriyama supo conquistar fronteras más allá de su natal Japón. Las aventuras de 
                Goku y sus amigos se hicieron tan populares que no sólo quedaron en libros y televisión, también alcanzaron 
                la cifra de 15 producciones cinematográficas que tomaron de referencia el argumento original de la saga.
            </p>
            
            <p style="color:white"> FullMetal Alchemist: Las aventuras de los hermanos Edward y Alphonse Elric, han tenido dos adaptaciones al
                anime, la primera a raíz de que superó a la historia original del manga, contó con un final diferente, el 
                cual no fue del agrado del autor, por lo tanto salió al aire una segunda adaptación que en este caso fue 
                más apegada a la historia original. Esta serie al igual que otras con mucha popularidad ha tenido 
                adaptación a diferentes medios y la más reciente es la película Live Action que será estrenada pronto. 
                La historia narra la travesía que viven estos hermanos con el fin de obtener la Piedra Filosofal para 
                devolverle el cuerpo al hermano más pequeño, el cual se perdió a causa de la Ley de la Alquimia sobre 
                equivalencia de intercambio al tratar de revivir a su madre.   
            </p>
           
            
            <p style="color:white"> Pokemon: no hay ninguna generación desde que salió su primer episodio que no conozca esta serie, 
                la cual ha tenido múltiples temporadas, haciendo lucir a su protagonista como alguien que posee el 
                secreto de la juventud eterna. El anime gracias a su gran popularidad entre el público infantil ha contado 
                con varias películas, y gozado de un gran marketing, en especial la imagen del adorable Pikachu.       
            </p>
             
            
            <p style="color:white"> One Piece: según el estudio desarrollado por Oricon, el manga One Piece reúne a la perfección los principales 
                fundamentos de la revista Shonen Jump, pionera en la difusión del género: el esfuerzo y la amistad en pos d
                el triunfo. El manga, creado por Eiichiro Oda, comenzó a publicarse en 1997, y llegó a convertirse en el 
                más vendido de la historia de Shonen Jump: más de 300 millones de lectores siguieron la historia 
                Monkey D. Luffy quien, luego de ser abandonado por su padre obtiene poderes mágicos al comer una “fruta 
                del diablo”.  
             </p>
