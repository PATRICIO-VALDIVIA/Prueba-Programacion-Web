         <h1 style="color:white">Categorias</h1>
         <p style="color:white"> Shonen: Típicamente se caracterizan por ser series con grandes dosis de acción, a menudo situaciones 
             humorísticas con protagonistas masculinos. El compañerismo entre adolescentes o adultos de un equipo de combate,
             también suele subrayarse en un shōnen. También suele haber personajes atractivos femeninos (como en el bishōjo 
             o fanservice), cabe destacar que no siempre. El arte de estilo de shōnen en general es menos florido que el 
             de shōjo, aunque esto varía mucho entre los mangakas(Creadores de los mangas). Shōnen no es un género específico 
             de manga, sino el término demográfico que se usa para referirse al manga y anime dirigido especialmente a hombres 
             jóvenes. Más allá del shōnen, está el término usado para los hombres adultos, el seinen. 
         </P>
         <p style="color:white"> Shōjo: Es dirigida especialmente al público femenino adolescente, pero que también puede interesar a los chicos. 
             El nombre proviene de la romanización del japonés 少女 (shōjo), literalmente «niña joven». No todo tiene que ver 
             con hombres guapos en este genero.El shōjo abarca una gran cantidad de temas y estilos, desde el drama 
             histórico hasta la ciencia ficción, y habitualmente hace énfasis en las relaciones humanas y sentimentales. 
             Estrictamente hablando, el shōjo no se adscribe a ningún estilo o género particular y es más un indicador 
             del público al que va destinado.
         </p>
       
         <p style="color:white"> Seinen: (青年? lit. «hombre adulto») es una categoría o género de manga y anime, enfocado hacia el público
             adulto pertenecientes al género masculino. Como tal, este tipo de anime tiende a ser mucho más sofisticado que 
             el shōnen. Algunos temas frecuentes son la violencia, la política, y situaciones extremas, en la vida de los 
             personajes que protagonizan la historia. Suele confundirse con el shōnen, aunque el seinen posee una variedad 
             más amplia en cuanto a estilos artísticos, con tramas más adultas pero sin llegar a escenas explícitas. 
             Hay un mayor énfasis en el argumento y como consecuencia menos importancia a la acción directa. 
             Los personajes y sus interacciones son a menudo más desarrolladas que en el shōnen.      
         </p>
         
         <p style="color:white"> Gore: es uno de los géneros del anime y manga caracterizado por el excesivo contenido de escenas sangríentas. 
             También conforme el paso del tiempo cuenta con una historia interesante y atractiva. Este género surgió con 
             Devilman en los 80's, del cual se fue expandiendo de Asia hasta América (este último por lo general aparece
             en películas); este género está incluido con algunos otros como acción, drama horror, misterio, psicológico,
             entre otros, en raras ocasiones se ha dado con la comedia (Akame ga KILL!) y el romance.
         </p>
        
         <p style="color:white"> Isekai: Subgénero de novelas ligeras, manga, anime y videojuegos de fantasía japonesa que giran en torno a una
             persona normal que es transportada y/o atrapada en un universo paralelo. En los últimos años es uno de los 
             géneros más populares, con exponentes como Sword Art Online.
         </p>
