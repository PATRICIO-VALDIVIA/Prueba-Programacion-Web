        <h3>Historia</h3>
         <p>La animación japonesa comercial data desde 1917 con una serie de cortometrajes similares a las producciones 
            americanas, y desde entonces la producción de obras de ánime en Japón ha seguido aumentando de manera constante.
            El estilo de arte propio y característico del ánime surgió en 1960 con las obras de Osamu Tezuka y se extendió a
            nivel internacional a finales del siglo XX, junto con el desarrollo de una audiencia nacional e internacional.
            El anime en Japón ahora se distribuye en cines, en emisiones televisivas directamente a los hogares, 
            por canales de cable, DVD, Blu-ray y masivamente a través de Internet para el resto del mundo; clasificándose 
            en numerosos géneros dirigidos a diversas audiencias generales y especializados.
