         <h3>¿Que son los OVA y ONA?</h3>
         <p>OVA: Abreviación de original video animation ('animación original en video')
            o también only video available ("solo disponible en vídeo"). Son producciones animadas destinadas para su 
            consumo en video que normalmente no se emiten en televisión. La duración de un episodio puede variar de unos 
            solos minutos a 45 minutos, y a veces pueden ser complementos de la historia. Es un capítulo especial
            (casi siempre no va de acuerdo con la historia) pero no es película. También es común que contengan un opening y
            un ending diferente a los de la serie.
             
         </P>
         <P>ONA: Es animación original creada de ánime especialmente 
            diseñado para su distribución en internet. El formato ONA aún no es muy usado y su duración es por lo 
            general corta. Un ejemplo de este es Azumanga Web Daioh y Candy Boy.
         </p>
